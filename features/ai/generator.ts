function slugify(value: string) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)+/g, "")
    .slice(0, 42);
}

function inferMarket(prompt: string) {
  const lower = prompt.toLowerCase();
  if (lower.includes("portfolio")) return "creative portfolio";
  if (lower.includes("fintech")) return "fintech operating system";
  if (lower.includes("agency")) return "modern growth agency";
  if (lower.includes("crypto")) return "crypto intelligence platform";
  if (lower.includes("ecommerce")) return "commerce brand";
  if (lower.includes("developer")) return "developer tools company";
  return "AI SaaS platform";
}

