export async function generateWebsite(input: unknown) {
  const parsed = generationPromptSchema.parse(input);

  if (!process.env.OPENAI_API_KEY) {
    return buildFallbackSite(parsed);
  }

  const openai = getOpenAI();
  const completion = await openai.chat.completions.create({
    model: process.env.OPENAI_MODEL || "gpt-4.1-mini",
    temperature: 0.65,
    response_format: { type: "json_object" },
    messages: [
      {
        role: "system",
        content:
          "Generate production website JSON for Nebula AI. Return only valid JSON with name, slug, prompt, palette, typography, seo, sections. Sections must use types navbar, hero, bento, features, stats, pricing, testimonials, faq, cta, footer.",
      },
      {
        role: "user",
        content: JSON.stringify(parsed),
      },
    ],
  });

  const raw = completion.choices[0]?.message.content;
  if (!raw) return buildFallbackSite(parsed);

  const json = JSON.parse(raw);
  return generatedSiteSchema.parse({
    ...json,
    prompt: parsed.prompt,
  });
}
